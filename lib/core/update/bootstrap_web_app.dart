function detectPlatform() {
  var ua = navigator.userAgent.toLowerCase();
  if (ua.indexOf('android') !== -1) return 'android';
  if (ua.indexOf('iphone') !== -1 || ua.indexOf('ipod') !== -1) return 'ios';
  if (ua.indexOf('ipad') !== -1) return 'ios';
  if (ua.indexOf('mac os x') !== -1 || ua.indexOf('macintosh') !== -1) {
    // iPadOS 13+ reports a desktop Mac UA; distinguish via touch support.
    if (navigator.maxTouchPoints && navigator.maxTouchPoints > 1) return 'ios';
    return 'macos';
  }
  if (ua.indexOf('windows') !== -1) return 'windows';
  if (ua.indexOf('linux') !== -1) return 'linux';
  return 'unknown';
}

