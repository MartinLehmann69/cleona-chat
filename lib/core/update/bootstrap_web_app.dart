// ============================================================================
// GF(256) Reed-Solomon decoder — byte-for-byte port of the Cauchy-matrix
// construction and Gaussian-elimination decode in
// lib/core/erasure/reed_solomon.dart (AES field, polynomial 0x11B / reduction
// constant 0x1B). Cross-checked at page load (rsSelfTest) against a vector
// generated by the real Dart ReedSolomon class.
// ============================================================================

var GF_MUL_TABLE = new Uint8Array(65536);
var GF_INV_TABLE = new Uint8Array(256);

function gfMulSlow(a, b) {
  a &= 0xFF; b &= 0xFF;
  var result = 0;
  for (var i = 0; i < 8; i++) {
    if (b & 1) result ^= a;
    var hi = a & 0x80;
    a = (a << 1) & 0xFF;
    if (hi) a ^= 0x1B;
    b >>= 1;
  }
  return result;
}

(function buildGfTables() {
  for (var a = 0; a < 256; a++) {
    for (var b = 0; b < 256; b++) {
      GF_MUL_TABLE[(a << 8) | b] = gfMulSlow(a, b);
    }
  }
  for (var a2 = 1; a2 < 256; a2++) {
    for (var b2 = 1; b2 < 256; b2++) {
      if (GF_MUL_TABLE[(a2 << 8) | b2] === 1) { GF_INV_TABLE[a2] = b2; break; }
    }
  }
})();

function gfMul(a, b) { return GF_MUL_TABLE[((a & 0xFF) << 8) | (b & 0xFF)]; }
function gfInv(a) { return GF_INV_TABLE[a & 0xFF]; }

// Cauchy coefficient: X[p] = p + 1, Y[i] = m + i + 1, coeff = 1 / (X[p] xor Y[i]).
function cauchyCoeff(m, p, i) {
  return gfInv(((p + 1) ^ (m + i + 1)) & 0xFF);
}

// Port of ReedSolomon.decode(). fragmentsMap: Map<fragmentIndex, Uint8Array>.
function rsDecode(fragmentsMap, originalSize, n, k) {
  var m = n - k;
  if (fragmentsMap.size < k) {
    throw new Error('Need at least ' + k + ' fragments, got ' + fragmentsMap.size);
  }

  var haveAllData = true;
  for (var i = 0; i < k; i++) {
    if (!fragmentsMap.has(i)) { haveAllData = false; break; }
  }
  if (haveAllData) {
    var fragSize0 = fragmentsMap.get(0).length;
    var result0 = new Uint8Array(fragSize0 * k);
    for (var i2 = 0; i2 < k; i2++) result0.set(fragmentsMap.get(i2), i2 * fragSize0);
    return result0.slice(0, originalSize);
  }

  var available = [];
  for (var i3 = 0; i3 < k; i3++) if (fragmentsMap.has(i3)) available.push(i3);
  for (var p = 0; p < m && available.length < k; p++) {
    if (fragmentsMap.has(k + p)) available.push(k + p);
  }
  if (available.length < k) {
    throw new Error('Need at least ' + k + ' fragments, got ' + available.length);
  }

  var fragSize = fragmentsMap.get(available[0]).length;

  var matrix = [];
  for (var r = 0; r < k; r++) {
    var rowIdx = available[r];
    var row = new Uint8Array(k);
    if (rowIdx < k) {
      row[rowIdx] = 1;
    } else {
      var pp = rowIdx - k;
      for (var c = 0; c < k; c++) row[c] = cauchyCoeff(m, pp, c);
    }
    matrix.push(row);
  }

  var w = 2 * k;
  var aug = [];
  for (var r2 = 0; r2 < k; r2++) {
    var row2 = new Uint8Array(w);
    row2.set(matrix[r2], 0);
    row2[k + r2] = 1;
    aug.push(row2);
  }

  for (var col = 0; col < k; col++) {
    var pivotRow = -1;
    for (var r3 = col; r3 < k; r3++) { if (aug[r3][col] !== 0) { pivotRow = r3; break; } }
    if (pivotRow < 0) throw new Error('Encoding matrix is singular — cannot decode');
    if (pivotRow !== col) { var tmp = aug[col]; aug[col] = aug[pivotRow]; aug[pivotRow] = tmp; }
    var pivotInv = gfInv(aug[col][col]);
    for (var c2 = 0; c2 < w; c2++) aug[col][c2] = gfMul(aug[col][c2], pivotInv);
    for (var r4 = 0; r4 < k; r4++) {
      if (r4 === col) continue;
      var factor = aug[r4][col];
      if (factor === 0) continue;
      for (var c3 = 0; c3 < w; c3++) aug[r4][c3] ^= gfMul(factor, aug[col][c3]);
    }
  }

  var inv = [];
  for (var r5 = 0; r5 < k; r5++) {
    var row3 = new Uint8Array(k);
    for (var c4 = 0; c4 < k; c4++) row3[c4] = aug[r5][k + c4];
    inv.push(row3);
  }

  var availArrs = available.map(function (idx) { return fragmentsMap.get(idx); });
  var result = new Uint8Array(fragSize * k);
  var fb = new Uint8Array(k);
  for (var j = 0; j < fragSize; j++) {
    for (var kk = 0; kk < k; kk++) fb[kk] = availArrs[kk][j];
    for (var d = 0; d < k; d++) {
      var invRow = inv[d];
      var val = 0;
      for (var kk2 = 0; kk2 < k; kk2++) {
        if (invRow[kk2] !== 0 && fb[kk2] !== 0) val ^= gfMul(invRow[kk2], fb[kk2]);
      }
      result[d * fragSize + j] = val;
    }
  }
  return result.slice(0, originalSize);
}

function rsSelfTest() {
  // Vector generated by the real lib/core/erasure/reed_solomon.dart
  // (ReedSolomon, n=10 k=7) — see the dart-side cross-check that produced
  // it: encode 57 bytes, then decode from 7 of the 10 fragments.
  var originalHex = '436c656f6e6120626f6f7473747261702052532063726f73732d636865636b20766563746f7220303132333435363738394142434445462121';
  var fragHex = {
    0: '436c656f6e6120626f', 1: '6f7473747261702052', 2: '532063726f73732d63',
    3: '6865636b2076656374', 4: '6f7220303132333435', 5: '363738394142434445', 6: '462121000000000000',
    7: 'd9847f66bc1213ca18', 8: '4fdb836240d86cc297', 9: '80868760d86d575899'
  };

  // General path: 4 data + 3 parity fragments, missing data 0/3/5 — forces
  // the Gaussian-elimination branch, not the fast concatenation path.
  var mixMap = new Map();
  [1, 2, 4, 6, 7, 8, 9].forEach(function (idx) { mixMap.set(idx, hexToBytes(fragHex[idx])); });
  var mixDecoded = rsDecode(mixMap, 57, 10, 7);
  if (bytesToHex(mixDecoded) !== originalHex) throw new Error('mixed-fragment decode mismatch');

  // Fast path: all K data fragments present.
  var fastMap = new Map();
  for (var i = 0; i < 7; i++) fastMap.set(i, hexToBytes(fragHex[i]));
  var fastDecoded = rsDecode(fastMap, 57, 10, 7);
  if (bytesToHex(fastDecoded) !== originalHex) throw new Error('fast-path decode mismatch');
}

// ============================================================================
// Ed25519 (RFC 8032) — extended twisted-Edwards coordinates, curve parameter
// a = -1 (so the unified Hisil/Wong/Carter/Dawson addition/doubling formulas
// below are complete: no special-cased doubling, no exceptional points).
// Non-cofactored verification: [S]B == R + [k]A. Cross-checked at page load
// (ed25519SelfTest) against vectors signed by the real
// lib/core/crypto/sodium_ffi.dart (SodiumFFI, i.e. libsodium itself).
// ============================================================================

var ED_P = (1n << 255n) - 19n;
var ED_L = (1n << 252n) + 27742317777372353535851937790883648493n;

function emod(a, m) {
  var r = a % m;
  return r >= 0n ? r : r + m;
}
function modPow(base, exp, m) {
  base = emod(base, m);
  var result = 1n;
  while (exp > 0n) {
    if (exp & 1n) result = emod(result * base, m);
    base = emod(base * base, m);
    exp >>= 1n;
  }
  return result;
}
function modInv(a, m) { return modPow(a, m - 2n, m); }

var ED_D = emod(-121665n * modInv(121666n, ED_P), ED_P);
var ED_SQRT_M1 = modPow(2n, (ED_P - 1n) / 4n, ED_P);

var ED_IDENTITY = { x: 0n, y: 1n, z: 1n, t: 0n };
var ED_BASE = (function () {
  var bx = 15112221349535400772501151409588531511454012693041857206046113283949847762202n;
  var by = 46316835694926478169428394003475163141307993866256225615783033603165251855960n;
  return { x: emod(bx, ED_P), y: emod(by, ED_P), z: 1n, t: emod(bx * by, ED_P) };
})();

function pointAdd(p1, p2) {
  var a = emod((p1.y - p1.x) * (p2.y - p2.x), ED_P);
  var b = emod((p1.y + p1.x) * (p2.y + p2.x), ED_P);
  var c = emod(2n * ED_D * p1.t * p2.t, ED_P);
  var d = emod(2n * p1.z * p2.z, ED_P);
  var e = emod(b - a, ED_P);
  var f = emod(d - c, ED_P);
  var g = emod(d + c, ED_P);
  var h = emod(b + a, ED_P);
  return { x: emod(e * f, ED_P), y: emod(g * h, ED_P), z: emod(f * g, ED_P), t: emod(e * h, ED_P) };
}

function pointDouble(p) {
  var a = emod(p.x * p.x, ED_P);
  var b = emod(p.y * p.y, ED_P);
  var c = emod(2n * p.z * p.z, ED_P);
  var d = emod(ED_P - a, ED_P); // curve parameter a = -1
  var xy = emod(p.x + p.y, ED_P);
  var e = emod(xy * xy - a - b, ED_P);
  var g = emod(d + b, ED_P);
  var f = emod(g - c, ED_P);
  var h = emod(d - b, ED_P);
  return { x: emod(e * f, ED_P), y: emod(g * h, ED_P), z: emod(f * g, ED_P), t: emod(e * h, ED_P) };
}

function pointMul(p, scalar) {
  var result = ED_IDENTITY;
  var addend = p;
  var e = scalar;
  while (e > 0n) {
    if (e & 1n) result = pointAdd(result, addend);
    addend = pointDouble(addend);
    e >>= 1n;
  }
  return result;
}

function bytesToBigIntLE(bytes) {
  var v = 0n;
  for (var i = bytes.length - 1; i >= 0; i--) v = (v << 8n) | BigInt(bytes[i]);
  return v;
}

function pointDecompress(bytes) {
  if (bytes.length !== 32) throw new Error('bad point length');
  var signBit = (bytes[31] >> 7) & 1;
  var yBytes = bytes.slice();
  yBytes[31] &= 0x7F;
  var y = bytesToBigIntLE(yBytes);
  if (y >= ED_P) throw new Error('y out of range');

  var y2 = emod(y * y, ED_P);
  var u = emod(y2 - 1n, ED_P);
  var v = emod(ED_D * y2 + 1n, ED_P);
  var v3 = emod(v * v * v, ED_P);
  var v7 = emod(v3 * v3 * v, ED_P);
  var uv7 = emod(u * v7, ED_P);
  var exp = (ED_P - 5n) / 8n;
  var x = emod(u * v3 * modPow(uv7, exp, ED_P), ED_P);

  var vx2 = emod(v * x * x, ED_P);
  if (vx2 === emod(u, ED_P)) {
    // x is already correct
  } else if (vx2 === emod(ED_P - u, ED_P)) {
    x = emod(x * ED_SQRT_M1, ED_P);
  } else {
    throw new Error('not a valid curve point');
  }

  if (x === 0n && signBit === 1) throw new Error('invalid point encoding');
  if ((x & 1n) !== BigInt(signBit)) x = emod(ED_P - x, ED_P);

  return { x: x, y: y, z: 1n, t: emod(x * y, ED_P) };
}

function pointEquals(p1, p2) {
  var xz1 = emod(p1.x * p2.z, ED_P);
  var xz2 = emod(p2.x * p1.z, ED_P);
  var yz1 = emod(p1.y * p2.z, ED_P);
  var yz2 = emod(p2.y * p1.z, ED_P);
  return xz1 === xz2 && yz1 === yz2;
}

async function ed25519Verify(message, signature, publicKey) {
  if (signature.length !== 64 || publicKey.length !== 32) return false;
  var rBytes = signature.slice(0, 32);
  var sBytes = signature.slice(32, 64);
  var s = bytesToBigIntLE(sBytes);
  if (s >= ED_L) return false; // reject non-canonical S (signature malleability)

  var a, r;
  try {
    a = pointDecompress(publicKey);
    r = pointDecompress(rBytes);
  } catch (e) {
    return false;
  }

  var hDigest = await sha512Bytes(concatBytes(rBytes, publicKey, message));
  var k = emod(bytesToBigIntLE(hDigest), ED_L);

  var sB = pointMul(ED_BASE, s);
  var kA = pointMul(a, k);
  var rhs = pointAdd(r, kA);

  return pointEquals(sB, rhs);
}

