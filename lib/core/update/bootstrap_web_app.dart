function nodeOrigin() { return window.location.protocol + '//' + window.location.host; }

async function fetchWithProgress(url, onProgress) {
  var resp = await fetch(url, { cache: 'no-store' });
  if (!resp.ok) {
    var err = new Error('HTTP ' + resp.status);
    err.status = resp.status;
    throw err;
  }
  var lengthHeader = resp.headers.get('Content-Length');
  var total = lengthHeader ? parseInt(lengthHeader, 10) : 0;
  if (!resp.body || !resp.body.getReader) {
    var buf = await resp.arrayBuffer();
    if (onProgress) onProgress(buf.byteLength, buf.byteLength || total);
    return new Uint8Array(buf);
  }
  var reader = resp.body.getReader();
  var chunks = [];
  var received = 0;
  for (;;) {
    var res = await reader.read();
    if (res.done) break;
    chunks.push(res.value);
    received += res.value.length;
    if (onProgress) onProgress(received, total);
  }
  var out = new Uint8Array(received);
  var offset = 0;
  chunks.forEach(function (c) { out.set(c, offset); offset += c.length; });
  return out;
}

async function headContentLength(url) {
  try {
    var resp = await fetch(url, { method: 'HEAD', cache: 'no-store' });
    if (!resp.ok) return null;
    var len = resp.headers.get('Content-Length');
    return len ? parseInt(len, 10) : null;
  } catch (e) {
    return null;
  }
}

async function downloadCompleteBinary(origin, platform) {
  var url = origin + '/cleona/binary/' + encodeURIComponent(platform);
  log('Trying complete binary: ' + url);
  setStep('fetch', 'active');
  return await fetchWithProgress(url, function (received, total) {
    var frac = total > 0 ? received / total : 0;
    setProgress(frac, formatBytes(received) + (total ? ' / ' + formatBytes(total) : ''));
  });
}

async function downloadViaFragments(origin, platform) {
  var rs = RS_PARAMS[platform];
  if (!rs) throw new Error('No Reed-Solomon parameters known for platform "' + platform + '"');
  var n = rs.n, k = rs.k;

  // Fragment endpoints carry no total-size metadata (§19.6.6), and the
  // Nostr binary-availability record (§19.6.5) does not include one either.
  // The original size is required to trim Reed-Solomon block padding, so a
  // HEAD to the complete-binary endpoint is the only way to learn it — this
  // succeeds whenever *any* directly reachable node advertises a complete
  // binary for this platform, which is the common bootstrap-phase case.
  var originalSize = await headContentLength(origin + '/cleona/binary/' + encodeURIComponent(platform));
  if (originalSize === null) {
    throw new Error('This node cannot report the exact binary size, so fragments cannot be safely trimmed after assembly');
  }

  var fragments = new Map();
  var order = [];
  for (var i = 0; i < k; i++) order.push(i);
  for (var p = k; p < n; p++) order.push(p);

  for (var idx = 0; idx < order.length && fragments.size < k; idx++) {
    var fragIndex = order[idx];
    var url = origin + '/cleona/fragment/' + encodeURIComponent(platform) + '/' + fragIndex;
    try {
      var data = await fetchWithProgress(url, function () {
        setProgress(fragments.size / k, fragments.size + '/' + k + ' fragments');
      });
      fragments.set(fragIndex, data);
      setProgress(fragments.size / k, fragments.size + '/' + k + ' fragments');
      log('Got fragment ' + fragIndex + ' (' + formatBytes(data.length) + ')');
    } catch (e) {
      log('Fragment ' + fragIndex + ' unavailable: ' + e.message);
    }
  }

  if (fragments.size < k) {
    throw new Error('Only got ' + fragments.size + ' of ' + k + ' required fragments');
  }

  setStatus('Assembling binary from ' + k + ' fragments…');
  return rsDecode(fragments, originalSize, n, k);
}

